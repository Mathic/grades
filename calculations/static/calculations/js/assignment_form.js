$(document).ready(function(){
	if($('#id_is_section').is(':checked')){
		$('#id_mark').parents('p').hide();
		$('#id_total').parents('p').hide();
	}
	$('#id_is_section').click(function(){
		$('#id_mark').parents('p').toggle(!this.checked);
		$('#id_total').parents('p').toggle(!this.checked);
	})
})
